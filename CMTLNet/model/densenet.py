class maskdense(nn.Module):
    def __init__(self, in_channels=3, name='densenet121'):
        super(maskdense, self).__init__()
        if name == 'densenet121':
            self.densnet_model = densenet121(in_channels=in_channels)
        elif name == 'densenet161':
            self.densnet_model = densenet161(in_channels=in_channels)
        else:
            self.densnet_model = None

        self.sigmoid = nn.Sigmoid()

        densnet = list(self.densnet_model.children())
        self.First_convolution = nn.Sequential(*densnet[0][0:1])
        self.First_BN = nn.Sequential(*densnet[0][1:2])
        self.Second_convolution = nn.Sequential(*densnet[0][2:4])
        self.denseblock1 = nn.Sequential(*densnet[0][4:5])
        self.transition1 = nn.Sequential(*densnet[0][5:6])
        self.denseblock2 = nn.Sequential(*densnet[0][6:7])
        self.transition2 = nn.Sequential(*densnet[0][7:8])
        self.denseblock3 = nn.Sequential(*densnet[0][8:9])
        self.transition3 = nn.Sequential(*densnet[0][9:10])
        self.denseblock4 = nn.Sequential(*densnet[0][10:11])
        self.norm5 = nn.Sequential(*densnet[0][11:12])

    def forward(self, x):
        x = self.First_convolution(x)
        x = self.First_BN(x)
        x_t1 = self.Second_convolution(x)

        x = self.denseblock1(x_t1)
        x_t2 = self.transition1(x)

        x = self.denseblock2(x_t2)
        x_t3 = self.transition2(x)

        x = self.denseblock3(x_t3)
        x_t4 = self.transition3(x)

        x = self.denseblock4(x_t4)
        x = self.norm5(x)
        x = F.relu(x, inplace=True)
        return x_t1, x_t2, x_t3, x_t4, x


def maskdense121(in_channels) -> DenseNet:
    return maskdense(in_channels=in_channels,name='densenet121')


def maskdense161(in_channels) -> DenseNet:
    return maskdense(in_channels=in_channels,  name='densenet161')


